const express = require('express');
const cors = require('cors');
const axios = require('axios');
const https = require('https');
const nodemailer = require('nodemailer');
require('dotenv').config();

const app = express();
const PORT = process.env.PORT || 5000;

const httpsAgent = new https.Agent({  
  rejectUnauthorized: false
});

// Middleware
app.use(cors());
app.use(express.json());

// GitHub API Route
app.get('/api/github/repos', async (req, res) => {
  try {
    const response = await axios.get('https://api.github.com/users/Guiilhermendes/repos', {
      params: {
        sort: 'updated',
        per_page: 6
      },
      httpsAgent
    });
    
    const repos = response.data.map(repo => ({
      id: repo.id,
      name: repo.name,
      description: repo.description,
      html_url: repo.html_url,
      language: repo.language,
      stargazers_count: repo.stargazers_count,
      forks_count: repo.forks_count,
      updated_at: repo.updated_at,
      topics: repo.topics
    }));
    
    res.json(repos);
  } catch (error) {
    console.error('Error fetching GitHub repos:', error);
    res.status(500).json({ error: 'Failed to fetch repositories' });
  }
});

// GitHub Stats Route
app.get('/api/github/stats', async (req, res) => {
  try {
    const response = await axios.get('https://api.github.com/users/Guiilhermendes', {
      httpsAgent
    });
    
    const stats = {
      public_repos: response.data.public_repos,
      followers: response.data.followers,
      following: response.data.following,
      bio: response.data.bio,
      location: response.data.location,
      avatar_url: response.data.avatar_url,
      name: response.data.name
    };
    
    res.json(stats);
  } catch (error) {
    console.error('Error fetching GitHub stats:', error);
    res.status(500).json({ error: 'Failed to fetch GitHub stats' });
  }
});

// Contact Form Route
app.post('/api/contact', async (req, res) => {
  try {
    const { name, email, message } = req.body;
    
    const transporter = nodemailer.createTransport({
      service: 'gmail',
      auth: {
        user: process.env.EMAIL_USER,
        pass: process.env.EMAIL_PASS
      },
      tls: {
        rejectUnauthorized: false
      }
    });

    const mailOptions = {
      from: email,
      to: 'guilhermemendeds@gmail.com',
      subject: `Portfolio Contact: ${name}`,
      html: `
        <h2>Nova mensagem do formul√°rio de contato</h2>
        <p><strong>Nome:</strong> ${name}</p>
        <p><strong>Email:</strong> ${email}</p>
        <p><strong>Mensagem:</strong></p>
        <p>${message}</p>
      `,
      replyTo: email
    };

    await transporter.sendMail(mailOptions);
    
    console.log('Contact form submission sent to email:', { name, email });
    
    res.json({ success: true, message: 'Message received successfully' });
  } catch (error) {
    console.error('Error processing contact form:', error);
    res.status(500).json({ error: 'Failed to send message' });
  }
});

app.listen(PORT, () => {
  console.log(`Server running on port ${PORT}`);
});
